from __future__ import print_function, division
from optparse import OptionParser
import os
import sys
import math
import vcf

sys.path.insert(0, os.path.abspath(os.path.dirname(__file__))[:-3] + 'python')
from Variant import *

__author__ = "Louis Dijkstra"

usage = """%prog <haplotype-file> <autosome>

Reads in a haplotype file <haplotype-file> as generated by 
'extract-haplotypes.py' and outputs all variant that are on 
the given autosome.
"""

def main():
	parser = OptionParser(usage=usage)
	(options, args) = parser.parse_args()

	if (len(args)!=2):
		parser.print_help()
		return 1

	haplo_file = open(args[0], 'r')
	autosome = int(args[1])

	if autosome < 1 or autosome > 23: 
		print("Given autosome is invalid. It must be an integer in {1,2,..,22}")
		sys.exit(main())

	for line in haplo_file:
		values = line.split()
		variant_autosome = int(values[0])	
		if variant_autosome != autosome:
			continue
		print(line, end='')
	
if __name__ == '__main__':
	sys.exit(main())


